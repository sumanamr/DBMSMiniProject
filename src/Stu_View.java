    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql="select * from login_record;";
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement stmt=con.createStatement();
            ResultSet rs=stmt.executeQuery(sql);

            while(rs.next())
           {
               String stud_id=String.valueOf(rs.getString("stud_id"));
               String place=String.valueOf(rs.getString("place"));
               String out_time=String.valueOf(rs.getString("out_time"));
               String in_time=String.valueOf(rs.getString("in_time"));
               String tbData[]={stud_id,place,out_time,in_time};
               DefaultTableModel tblModel=(DefaultTableModel)jTable1.getModel();
               tblModel.addRow(tbData);
           }
               
           con.close();
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

